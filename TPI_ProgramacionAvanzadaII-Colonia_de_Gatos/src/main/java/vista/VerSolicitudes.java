    private void ACEPTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ACEPTARActionPerformed
        try {
            SolicitudAdopcion sol = obtenerSolicitudDeTabla();
            String[] opciones = {"Temporal (Hogar no Aprobado)", "Definitiva (Hogar Aprobado)"};
            int seleccion = JOptionPane.showOptionDialog(this, "Seleccione el tipo de aprobacion para la solicitud #" + sol.getIdSolicitud() +"\n(Familia: " + sol.getFamilia().getNombre() + ")", "Confirmar Aprobacion", JOptionPane.DEFAULT_OPTION,JOptionPane.QUESTION_MESSAGE,null,opciones, opciones[0]);
            int tipoAdopcion = -1;
            if (seleccion == 0) tipoAdopcion = 1; 
            if (seleccion == 1) tipoAdopcion = 2; 
            if (tipoAdopcion == -1) {
                JOptionPane.showMessageDialog(this, "Accion cancelada.", "Cancelado", JOptionPane.INFORMATION_MESSAGE);
                return; 
            }
            servicios.adopcionService.aprobarSolicitud(sol, tipoAdopcion); 
            JOptionPane.showMessageDialog(this, "Solicitud APROBADA.", "exito", JOptionPane.INFORMATION_MESSAGE);
            cargarDatosTabla(servicios.adopcionService.verPendientes());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error al Aprobar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ACEPTARActionPerformed

    private void RECHAZARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RECHAZARActionPerformed
        try {
            SolicitudAdopcion sol = obtenerSolicitudDeTabla();
            int confirm = JOptionPane.showConfirmDialog(this,"Seguro que deseas rechazar esta solicitud?","Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION) {
                servicios.adopcionService.rechazarSolicitud(sol);
                JOptionPane.showMessageDialog(this, "Solicitud rechazada. El gato vuelve a estar disponible.", "exito", JOptionPane.INFORMATION_MESSAGE);
                 cargarDatosTabla(servicios.adopcionService.verPendientes());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error al Rechazar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_RECHAZARActionPerformed

