    private void configurarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override public boolean isCellEditable(int row, int column) { return false; }
        };
        
        modeloTabla.addColumn("ID Solicitud");
        modeloTabla.addColumn("Fecha");
        modeloTabla.addColumn("Gato (ID)");
        modeloTabla.addColumn("Familia (DNI)");
        modeloTabla.addColumn("Estado");
        tablaSolicitudes.setModel(modeloTabla);
    }
    private void cargarDatosTabla(List<SolicitudAdopcion> solicitudes) {
        modeloTabla.setRowCount(0);
        for (SolicitudAdopcion s : solicitudes) {
            Object[] fila = {s.getIdSolicitud(),(s.getFechaSolicitud() != null) ? s.getFechaSolicitud().format(FORMATO_FECHA) : "N/A",(s.getGato() != null) ? s.getGato().getNombre() + " (ID:" + s.getGato().getId() + ")" : "N/A",(s.getFamilia() != null) ? s.getFamilia().getNombre() + " (DNI:" + s.getFamilia().getDNI() + ")" : "N/A",s.getEstadoComoTexto() };
            modeloTabla.addRow(fila);
        }
    }
    private SolicitudAdopcion obtenerSolicitudDeTabla() throws Exception {
         int filaSel = tablaSolicitudes.getSelectedRow();
         if (filaSel == -1) {
             throw new Exception("Debe seleccionar una solicitud de la tabla.");
         }
         int idSol = (int) modeloTabla.getValueAt(filaSel, 0); 
         SolicitudAdopcion solicitud = servicios.adopcionService.solicitudPorId(idSol);
         if (solicitud == null) {
             cargarDatosTabla(servicios.adopcionService.verPendientes()); 
             throw new Exception("La solicitud seleccionada ya no existe.");
         }
         return solicitud;
    }
