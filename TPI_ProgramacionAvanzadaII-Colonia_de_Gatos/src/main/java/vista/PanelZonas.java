    private void configurarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };

        modeloTabla.addColumn("ID");
        modeloTabla.addColumn("Nombre de Zona");
        modeloTabla.addColumn("Coordenadas");
        modeloTabla.addColumn("Tipo");

        
        jTable1.setModel(modeloTabla);
    }
    private void cargarDatosTabla() {
        modeloTabla.setRowCount(0); 
        List<Zona_geografica> zonas = servicios.zonaService.listarZonas();

        for (Zona_geografica zona : zonas) {
            String tipoStr = switch (zona.getTipo()) {
                case 1 -> "Hogar";
                case 2 -> "Tarea";
                case 3 -> "Gato Encontrado";
                default -> "Desconocido";
            };
            
            Object[] fila = {
                zona.getIdzona(),
                zona.getNombrezona(),
                zona.getCoordenadas(),
                tipoStr
            };
            modeloTabla.addRow(fila);
        }
    }
