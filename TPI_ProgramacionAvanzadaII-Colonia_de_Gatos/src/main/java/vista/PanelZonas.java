    private void MODIFICARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MODIFICARActionPerformed
        int filaSel = jTable1.getSelectedRow();
        if (filaSel == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una zona de la tabla para modificar.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idZona = (int) modeloTabla.getValueAt(filaSel, 0);
        Zona_geografica zona = servicios.zonaService.buscarPorId(idZona);

        if (zona == null) {
            JOptionPane.showMessageDialog(this, "No se pudo encontrar la zona seleccionada.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JTextField txtNombreZona = new JTextField(zona.getNombrezona());
        JTextField txtCoords = new JTextField(zona.getCoordenadas());
        JComboBox<String> cmbTipoDialog = new JComboBox<>(new String[]{"Hogar", "Tarea", "Gato Encontrado"});
        cmbTipoDialog.setSelectedIndex(zona.getTipo() - 1); 

        JPanel dialogPanel = new JPanel(new GridLayout(3, 2, 5, 5));
        dialogPanel.add(new JLabel("Nombre de Zona:"));
        dialogPanel.add(txtNombreZona);
        dialogPanel.add(new JLabel("Coordenadas:"));
        dialogPanel.add(txtCoords);
        dialogPanel.add(new JLabel("Tipo:"));
        dialogPanel.add(cmbTipoDialog);

        int result = JOptionPane.showConfirmDialog(this, dialogPanel, "Modificar Zona (ID: " + idZona + ")",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            try {
                String nuevoNombre = txtNombreZona.getText();
                String nuevasCoords = txtCoords.getText();
                int nuevoTipo = cmbTipoDialog.getSelectedIndex() + 1;

                
                servicios.zonaService.actualizarZona(idZona, nuevoNombre, nuevasCoords, nuevoTipo);
                
                JOptionPane.showMessageDialog(this, "Zona actualizada con exito.", "exito", JOptionPane.INFORMATION_MESSAGE);
                cargarDatosTabla(); 

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_MODIFICARActionPerformed

    private void ELIMINARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ELIMINARActionPerformed
        int filaSel = jTable1.getSelectedRow();
        if (filaSel == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una zona de la tabla para eliminar.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int idZona = (int) modeloTabla.getValueAt(filaSel, 0);
        String nombreZona = (String) modeloTabla.getValueAt(filaSel, 1);
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Seguro que deseas eliminar la zona '" + nombreZona + "' (ID: " + idZona + ")?",
            "Confirmar Eliminaci√≥n", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Zona_geografica zona = servicios.zonaService.buscarPorId(idZona);
                servicios.zonaService.eliminarZona(zona);
                
                JOptionPane.showMessageDialog(this, "Zona eliminada con exito.", "exito", JOptionPane.INFORMATION_MESSAGE);
                cargarDatosTabla(); 
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ELIMINARActionPerformed

    private void REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_REGISTRARActionPerformed

        JTextField txtNombreZona = new JTextField(jTextField1.getText()); 
        JTextField txtCoords = new JTextField();
        JComboBox<String> cmbTipoDialog = new JComboBox<>(new String[]{"Seleccione...", "Hogar", "Tarea", "Gato Encontrado"});

        JPanel dialogPanel = new JPanel(new GridLayout(3, 2, 5, 5));
        dialogPanel.add(new JLabel("Nombre de Zona:"));
        dialogPanel.add(txtNombreZona);
        dialogPanel.add(new JLabel("Coordenadas:"));
        dialogPanel.add(txtCoords);
        dialogPanel.add(new JLabel("Tipo:"));
        dialogPanel.add(cmbTipoDialog);

        int result = JOptionPane.showConfirmDialog(this, dialogPanel, "Registrar Nueva Zona",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            try {
                String nombre = txtNombreZona.getText();
                String coords = txtCoords.getText();
                int tipo = cmbTipoDialog.getSelectedIndex(); 

                if (tipo == 0) {
                    throw new Exception("Debe seleccionar un tipo valido.");
                }
                servicios.zonaService.registrarZona(nombre, coords, tipo);
                
                JOptionPane.showMessageDialog(this, "Zona registrada con exito.", "exito", JOptionPane.INFORMATION_MESSAGE);
                cargarDatosTabla(); 
                
               
                jTextField1.setText("");
                jTextField2.setText("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al registrar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_REGISTRARActionPerformed

    private void BUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUSCARActionPerformed
        try {
            String idStr = jTextField2.getText();
            if (idStr.trim().isEmpty()) {
                cargarDatosTabla();
                JOptionPane.showMessageDialog(this, "Mostrando todas las zonas.", "Busqueda", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            int id = Integer.parseInt(idStr);
            Zona_geografica zona = servicios.zonaService.buscarPorId(id);
            
            if (zona != null) {
                modeloTabla.setRowCount(0); 
                String tipoStr = switch (zona.getTipo()) {
                    case 1 -> "Hogar";
                    case 2 -> "Tarea";
                    case 3 -> "Gato Encontrado";
                    default -> "Desconocido";
                };
                Object[] fila = { zona.getIdzona(), zona.getNombrezona(), zona.getCoordenadas(), tipoStr };
                modeloTabla.addRow(fila);
                
            } else {
                JOptionPane.showMessageDialog(this, "No se encontro ninguna zona con el ID: " + id, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ingrese un ID  valido", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BUSCARActionPerformed


