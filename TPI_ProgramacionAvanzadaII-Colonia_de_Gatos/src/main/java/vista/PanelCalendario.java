    private void cargarDatosTabla(List<EventoCalendario> eventos) {
        modeloTabla.setRowCount(0); 
        
        for (EventoCalendario evento : eventos) {
            String tipoEvento = evento.getClass().getSimpleName();
            String estado = "Desconocido";
            String encargado = "N/A"; 
            if (evento.estaVencido()) {
                estado = "VENCIDO";
            } else if (evento.requiereAlerta()) {
                estado = "PrÃ³ximo a Vencer";
            } else {
                estado = "Pendiente";
            }
            if (evento instanceof Tarea tarea) {
                estado = switch (tarea.getEstado()) {
                    case 0 -> estado; 
                    case 1 -> "Completado";
                    case 2 -> "Incompleta";
                    default -> "N/A";
                };
                encargado = (tarea.getVoluntario() != null) ? tarea.getVoluntario().getNombre() : "SIN ASIGNAR";
            } else if (evento instanceof Diagnosticos diag) {
                if (diag.isEstado()) estado = "Completado";
                encargado = (diag.getAutor() != null) ? diag.getAutor().getNombre() : "N/A";
            } else if (evento instanceof Visita visita) {
                if (visita.isCumplida()) estado = "Completado";
                encargado = (visita.getVoluntario() != null) ? visita.getVoluntario().getNombre() : "N/A";
            }
            Object[] fila = {(evento.getFecha() != null) ? evento.getFecha().format(FORMATO_FECHA):"N/A",tipoEvento,evento.getDescripcion(),encargado, estado};
            modeloTabla.addRow(fila);
        }
    }

