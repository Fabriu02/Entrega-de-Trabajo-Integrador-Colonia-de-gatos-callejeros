    private void rolItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_rolItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (!txtIDUSUARIO.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Limpia el campo 'ID USUARIO' antes de filtrar por rol.", "Error en busqueda", JOptionPane.WARNING_MESSAGE);
               rol.setSelectedIndex(0);
                cargarDatosTabla(servicios.usuarioService.listarUsuarios());
                return;
            }
            int rolSeleccionado = rol.getSelectedIndex(); 
            if (rolSeleccionado == 0) {
                cargarDatosTabla(servicios.usuarioService.listarUsuarios());
            } else {
                List<Usuario> todos = servicios.usuarioService.listarUsuarios();
                List<Usuario> filtrados = new ArrayList<>();
                for (Usuario u : todos) {
                    if (u.getRol() == rolSeleccionado) {
                        filtrados.add(u);
                    }
                }
                cargarDatosTabla(filtrados);
            }
        }
    }//GEN-LAST:event_rolItemStateChanged


