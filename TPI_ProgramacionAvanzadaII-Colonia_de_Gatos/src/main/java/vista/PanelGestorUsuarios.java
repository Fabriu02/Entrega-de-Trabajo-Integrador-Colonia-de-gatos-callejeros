    private void configurarTabla() {
        modeloTabla = new DefaultTableModel() {
            @Override public boolean isCellEditable(int row, int column) { return false; }
        };
        
        modeloTabla.addColumn("DNI");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Rol");
        modeloTabla.addColumn("Telefono");
        modeloTabla.addColumn("Correo");
        modeloTabla.addColumn("Reputacion");
        
        tablaUsuarios.setModel(modeloTabla); 
    }
    private void cargarDatosTabla(List<Usuario> usuarios) {
        modeloTabla.setRowCount(0);
        for (Usuario u : usuarios) {
            String reputacionStr = "N/A";
            if (u instanceof Sistema_reputacion) {
                reputacionStr = String.valueOf(((Sistema_reputacion) u).getReputacion());
            }
            Object[] fila = {u.getDNI(),u.getNombre(),u.nombrerol(),u.getTelefono(), u.getCorreo(),reputacionStr};
            modeloTabla.addRow(fila);
        }
    }
        private void cargarRolComboBox(){
            DefaultComboBoxModel<String> comboModel = new DefaultComboBoxModel<>();
            comboModel.addElement("TODOS LOS ROLES"); 
            comboModel.addElement("Administrador"); 
            comboModel.addElement("Veterinario"); 
            comboModel.addElement("Voluntario"); 
            comboModel.addElement("Familia"); 

            rol.setRenderer(new RolComboBoxRenderer());
            rol.setModel(comboModel);
        }
        private Usuario obtenerUsuarioDeTabla() throws Exception {
         int filaSel = tablaUsuarios.getSelectedRow();
         if (filaSel == -1) {
             throw new Exception("Debe seleccionar un usuario de la tabla.");
         }
         String dni = (String) modeloTabla.getValueAt(filaSel, 0); 
         Usuario usuario = servicios.usuarioService.UsuarioporDNI(dni);
         if (usuario == null) {
             cargarDatosTabla(servicios.usuarioService.listarUsuarios()); 
             throw new Exception("El usuario seleccionado ya no existe. La tabla ha sido actualizada.");
         }
         return usuario;
    }
        private void limpiarFiltros() {
        txtIDUSUARIO.setText("");
        rol.setSelectedIndex(0);
    }

        
    
